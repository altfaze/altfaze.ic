// Generate flat array of all keywords
export const ALL_KEYWORDS = Object.values(KEYWORDS_DATABASE).flatMap(
  category => category.keywords
)

// Get keywords by category
export function getKeywordsByCategory(category: string) {
  return KEYWORDS_DATABASE[category as keyof typeof KEYWORDS_DATABASE]
}

// Get random keywords for dynamic content
export function getRandomKeywords(count: number = 5) {
  return ALL_KEYWORDS
    .sort(() => Math.random() - 0.5)
    .slice(0, count)
}

// Get high-intent conversion keywords
export function getHighIntentKeywords() {
  return KEYWORDS_DATABASE.highIntentConversion.keywords
}

// Get related keywords
export function getRelatedKeywords(keyword: string, count: number = 5) {
  return ALL_KEYWORDS
    .filter(k => k !== keyword)
    .filter(k => 
      k.split(" ").some(word => keyword.includes(word))
    )
    .slice(0, count)
}

